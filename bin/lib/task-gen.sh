#!/usr/bin/env bash
#
# task-gen.sh — Generate task YAML and prompts for lab-action commands
#
# Source guard
[[ -n "${_TASK_GEN_SH_LOADED:-}" ]] && return 0
_TASK_GEN_SH_LOADED=1

_TASK_GEN_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
_TASK_GEN_LAB_ROOT="$(cd "${_TASK_GEN_DIR}/../.." && pwd)"

source "${_TASK_GEN_LAB_ROOT}/harness/lib/common.sh"
source "${_TASK_GEN_DIR}/incidents.sh"

# ---------------------------------------------------------------------------
# generate_investigate_task <incident_id>
#
# Generates a read-only task YAML and prompt for CI failure investigation.
# Prints the task file path on stdout; logs to stderr.
# ---------------------------------------------------------------------------
generate_investigate_task() {
  local incident_id="$1"

  local incident_json
  incident_json=$(incident_get "$incident_id")
  [[ -z "$incident_json" || "$incident_json" == "null" ]] \
    && die "Incident not found: ${incident_id}"

  local repo signature workflow run_ids
  repo=$(echo "$incident_json" | jq -r '.repo')
  signature=$(echo "$incident_json" | jq -r '.signature')
  workflow=$(echo "$incident_json" | jq -r '.workflow')
  run_ids=$(echo "$incident_json" | jq -r '.run_ids | join(", ")')

  local task_id
  task_id="investigate-$(printf '%s' "$incident_id" | tr '[:upper:]' '[:lower:]')"
  local repo_url="https://github.com/${repo}.git"

  # --- prompt file ---
  local prompt_file
  prompt_file=$(mktemp /tmp/lab-action-prompt.XXXXXX)

  {
    printf '# Incident Context: %s\n\n' "$incident_id"
    printf -- '- **Incident ID:** %s\n' "$incident_id"
    printf -- '- **Repository:** %s\n' "$repo"
    printf -- '- **Workflow:** %s\n' "$workflow"
    printf -- '- **Failure signature:** %s\n' "$signature"
    printf -- '- **GitHub Actions run IDs:** %s\n\n' "$run_ids"
    printf 'You can use `gh run view <run_id> --repo %s` to inspect CI logs.\n\n' "$repo"
    printf -- '---\n\n'
    cat "${_TASK_GEN_LAB_ROOT}/prompts/claude/investigate.md"
  } > "$prompt_file"

  # --- task YAML ---
  local task_file
  task_file=$(mktemp /tmp/lab-action-task.XXXXXX)
  local safe_workflow
  safe_workflow=$(printf '%s' "$workflow" | tr '"' "'")

  cat > "$task_file" <<YAML_EOF
task_id: ${task_id}
title: "Investigate ${incident_id}: ${safe_workflow} failure"
mode: read-only
repo_url: "${repo_url}"
base_ref: main
agent: claude-code
prompt_file: "${prompt_file}"
max_runtime_minutes: 10
verification_commands:
  - "test -f agent-output.md"
expected_outputs:
  - "agent-output.md"
notes: >
  Investigation of ${incident_id} in ${repo}.
  Generated by lab-action investigate.
YAML_EOF

  log_success "Generated investigate task: ${task_file}" >&2
  echo "$task_file"
}

# ---------------------------------------------------------------------------
# generate_review_pr_task <owner> <repo> <pr_number>
#
# Fetches PR details and diff via gh, generates a review-only task YAML
# and a prompt containing the diff. Prints the task file path on stdout.
# ---------------------------------------------------------------------------
generate_review_pr_task() {
  local owner="$1" repo="$2" pr_number="$3"

  require_command gh
  require_command jq

  # Fetch PR metadata
  log_info "Fetching PR #${pr_number} from ${owner}/${repo}..." >&2
  local pr_json
  pr_json=$(gh pr view "$pr_number" --repo "${owner}/${repo}" \
    --json title,author,body,baseRefName,headRefName,url 2>/dev/null) \
    || die "Failed to fetch PR. Check: gh auth status && gh pr view ${pr_number} --repo ${owner}/${repo}"

  local pr_title pr_author pr_body base_ref head_ref pr_url
  pr_title=$(echo "$pr_json" | jq -r '.title')
  pr_author=$(echo "$pr_json" | jq -r '.author.login')
  pr_body=$(echo "$pr_json" | jq -r '.body // ""')
  base_ref=$(echo "$pr_json" | jq -r '.baseRefName')
  head_ref=$(echo "$pr_json" | jq -r '.headRefName')
  pr_url=$(echo "$pr_json" | jq -r '.url')

  local task_id="review-pr-${repo}-${pr_number}"
  local repo_url="https://github.com/${owner}/${repo}.git"

  # --- prompt file ---
  local prompt_file
  prompt_file=$(mktemp /tmp/lab-action-prompt.XXXXXX)

  {
    printf '# Task: Pull Request Review — %s/%s#%s\n\n' "$owner" "$repo" "$pr_number"
    printf 'Review the following pull request and produce a structured review in `agent-output.md`.\n\n'
    printf '## PR Details\n\n'
    printf -- '- **Title:** %s\n' "$pr_title"
    printf -- '- **Author:** %s\n' "$pr_author"
    printf -- '- **URL:** %s\n' "$pr_url"
    printf -- '- **Base:** %s ← %s\n\n' "$base_ref" "$head_ref"
    printf '## PR Description\n\n'
    if [[ -n "$pr_body" ]]; then
      printf '%s\n\n' "$pr_body"
    else
      printf '_No description provided._\n\n'
    fi
    printf '## Changes (diff)\n\n```diff\n'
  } > "$prompt_file"

  # Append diff directly from gh (handles large diffs without storing in variable)
  log_info "Fetching PR diff..." >&2
  gh pr diff "$pr_number" --repo "${owner}/${repo}" >> "$prompt_file" 2>/dev/null \
    || die "Failed to fetch PR diff"

  # Append review instructions (single-quoted delimiter = no expansion)
  cat >> "$prompt_file" << 'REVIEW_EOF'
```

---

## Review structure

Organize findings by severity:

### Critical
Issues that could cause data loss, security vulnerabilities, or production outages.

### High
Bugs, missing error handling, or logic errors that will affect users.

### Medium
Code quality issues, missing edge cases, or maintainability concerns.

### Low
Style inconsistencies, minor improvements, or documentation gaps.

## For each finding, include

- **Location:** file path and line range
- **Issue:** what is wrong or risky
- **Recommendation:** how to fix it
- **Confidence:** high, medium, or low

## Constraints

- **Review-only.** Do not modify any files.
- Write all output to `agent-output.md`.
- Focus on the PR changes shown in the diff above. Use the codebase for context.
- If you find no issues in a severity category, say so.
- Reference the surrounding code to evaluate whether the changes are correct.
REVIEW_EOF

  # --- task YAML ---
  local task_file safe_title
  task_file=$(mktemp /tmp/lab-action-task.XXXXXX)
  safe_title=$(printf '%s' "$pr_title" | tr '"' "'" | cut -c1-80)

  cat > "$task_file" <<YAML_EOF
task_id: ${task_id}
title: "Review PR #${pr_number}: ${safe_title}"
mode: review-only
repo_url: "${repo_url}"
base_ref: ${base_ref}
agent: claude-code
prompt_file: "${prompt_file}"
max_runtime_minutes: 10
verification_commands:
  - "test -f agent-output.md"
expected_outputs:
  - "agent-output.md"
notes: >
  Automated PR review of ${owner}/${repo}#${pr_number}.
  Generated by lab-action review-pr.
YAML_EOF

  log_success "PR context ready: ${pr_title}" >&2
  echo "$task_file"
}

# ---------------------------------------------------------------------------
# generate_draft_fix_task <incident_id>
#
# Generates a patch-only task YAML and prompt for creating a fix.
# Includes prior diagnosis if available. Prints the task file path.
# ---------------------------------------------------------------------------
generate_draft_fix_task() {
  local incident_id="$1"

  local incident_json
  incident_json=$(incident_get "$incident_id")
  [[ -z "$incident_json" || "$incident_json" == "null" ]] \
    && die "Incident not found: ${incident_id}"

  local repo signature workflow diagnosis
  repo=$(echo "$incident_json" | jq -r '.repo')
  signature=$(echo "$incident_json" | jq -r '.signature')
  workflow=$(echo "$incident_json" | jq -r '.workflow')
  diagnosis=$(echo "$incident_json" | jq -r '.diagnosis // ""')

  local task_id
  task_id="fix-$(printf '%s' "$incident_id" | tr '[:upper:]' '[:lower:]')"
  local repo_url="https://github.com/${repo}.git"

  # --- prompt file ---
  local prompt_file
  prompt_file=$(mktemp /tmp/lab-action-prompt.XXXXXX)

  {
    printf '# Incident Context: %s\n\n' "$incident_id"
    printf -- '- **Incident ID:** %s\n' "$incident_id"
    printf -- '- **Repository:** %s\n' "$repo"
    printf -- '- **Workflow:** %s\n' "$workflow"
    printf -- '- **Failure signature:** %s\n\n' "$signature"
    if [[ -n "$diagnosis" ]]; then
      printf '## Prior Diagnosis\n\n%s\n\n' "$diagnosis"
    fi
    printf -- '---\n\n'
    cat "${_TASK_GEN_LAB_ROOT}/prompts/claude/bugfix-patch-only.md"
  } > "$prompt_file"

  # --- task YAML ---
  local task_file safe_workflow
  task_file=$(mktemp /tmp/lab-action-task.XXXXXX)
  safe_workflow=$(printf '%s' "$workflow" | tr '"' "'")

  cat > "$task_file" <<YAML_EOF
task_id: ${task_id}
title: "Fix ${incident_id}: ${safe_workflow} failure"
mode: patch-only
repo_url: "${repo_url}"
base_ref: main
agent: claude-code
prompt_file: "${prompt_file}"
max_runtime_minutes: 15
verification_commands:
  - "test -f agent-output.md"
  - "test -f changed-files.txt"
expected_outputs:
  - "agent-output.md"
  - "changed-files.txt"
notes: >
  Draft fix for ${incident_id} in ${repo}.
  Generated by lab-action draft-fix.
YAML_EOF

  log_success "Generated draft-fix task: ${task_file}" >&2
  echo "$task_file"
}
